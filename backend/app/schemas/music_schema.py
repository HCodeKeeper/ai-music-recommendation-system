from marshmallow import Schema, fields, validate

class MusicSchema(Schema):
    track_id = fields.Str(dump_only=True)
    title = fields.Str(required=True, validate=validate.Length(max=255))
    artist = fields.Str(required=True, validate=validate.Length(max=255))
    album = fields.Str(validate=validate.Length(max=255))
    duration = fields.Float()
    genre = fields.Str(validate=validate.Length(max=100))
    year = fields.Int()
    popularity = fields.Float()
    danceability = fields.Float()
    energy = fields.Float()
    valence = fields.Float()
    tempo = fields.Float()
    acousticness = fields.Float()
    instrumentalness = fields.Float()
    liveness = fields.Float()
    speechiness = fields.Float()
    file_path = fields.Str(validate=validate.Length(max=500))

